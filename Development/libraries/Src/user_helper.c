/*****************************************************************************************************
 * @fn									- h_strlen
 *
 * @brief								- This function will be used find length of the string
 *
 * @param[const char* str]				- corresponding char* string
 *
 * @return								- length of the input string
 *
 * @note								- None
 ******************************************************************************************************/
size_t h_strlen(const char* str) {
    size_t len = 0;
    while (*str != '\0') {
        len++;
        str++;
    }
    return len;
}
